/* External memory allocator refs */
extern void *(*caller_malloc_ref)(size_t);
extern void (*caller_free_ref)(void *);

/**
 * Test 1: RSA-PSS Verify
 * Uses OpenSSL-generated test vector to verify PSS padding implementation
 * Message: "hello world"
 * Generated using: gcc generate_test_vector.c -o generate_test_vector -lcrypto
 */
static bool test_pss_verify(void)
{
    /* Message hash: SHA-256("hello world") */
    const uint8_t mhash[32] = {
        0xb9, 0x4d, 0x27, 0xb9, 0x93, 0x4d, 0x3e, 0x08,
        0xa5, 0x2e, 0x52, 0xd7, 0xda, 0x7d, 0xab, 0xfa,
        0xc4, 0x84, 0xef, 0xe3, 0x7a, 0x53, 0x80, 0xee,
        0x90, 0x88, 0xf7, 0xac, 0xe2, 0xef, 0xcd, 0xe9};

    /* Expected encoded message constructed using OpenSSL SHA-256 and controlled salt */
    const uint8_t expected_encoded_msg[128] = {
        0x64, 0x49, 0xb1, 0x76, 0x73, 0x97, 0xab, 0x39,
        0x4a, 0x78, 0x8c, 0x5a, 0x78, 0xf2, 0xa7, 0x90,
        0xc7, 0xdf, 0xeb, 0xec, 0xca, 0x32, 0x91, 0x8e,
        0xdd, 0x9d, 0x0e, 0x7f, 0x86, 0xd5, 0x5a, 0x6d,
        0x43, 0x23, 0x94, 0xc5, 0xb0, 0x64, 0xbf, 0x02,
        0xf1, 0x77, 0x08, 0xf6, 0xa9, 0x54, 0x81, 0xde,
        0x59, 0xc4, 0xe0, 0x06, 0xae, 0xef, 0x47, 0xee,
        0x53, 0xce, 0xe8, 0xe2, 0x39, 0x03, 0xa7, 0x85,
        0x98, 0x04, 0xf0, 0x12, 0xfa, 0xcb, 0x79, 0x38,
        0xe6, 0xa6, 0xd2, 0xea, 0x46, 0xb6, 0x20, 0x6d,
        0xe4, 0x3d, 0xd0, 0x3f, 0xce, 0xf6, 0x73, 0x47,
        0x62, 0x11, 0x1f, 0xeb, 0x38, 0xff, 0x72, 0xb6,
        0xe4, 0x7f, 0x63, 0xb4, 0x41, 0x4b, 0x04, 0xe6,
        0x48, 0x0f, 0x85, 0x41, 0xff, 0x62, 0x3f, 0x34,
        0x53, 0x86, 0x33, 0xbe, 0x7f, 0x17, 0x7c, 0xf1,
        0x30, 0xb7, 0xb0, 0xd9, 0x14, 0x55, 0x83, 0xbc};

    const size_t em_len = 128;     /* 1024-bit modulus */
    const uint16_t em_bits = 1023; /* modBits - 1 */

    /* Test PSS Verify against OpenSSL-generated encoding */
    bool verify_ok = tls_rsa_pss_verify(
        expected_encoded_msg, em_len, em_bits,
        mhash, sizeof(mhash),
        TLS_HASH_SHA256);

    return verify_ok;
}

int main(void)
{
    os_ClrHome();

    /* Initialize lwIP memory */
    caller_malloc_ref = malloc;
    caller_free_ref = free;
    mem_init();

    /* Initialize TLS context */
    if (!tls_init())
    {
        printf("TLS init failed\n");
        os_GetKey();
        return 1;
    }

    /* Run tests */
    bool test1 = test_pss_verify();

    /* Output results */
    os_ClrHome();
    printf("Test 1 (Verify): %s\n", test1 ? "success" : "fail");
    os_GetKey();

    /* Cleanup */
    tls_cleanup();

    return test1 ? 0 : 1;
}
