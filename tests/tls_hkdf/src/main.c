/**
 * RFC 5869 Test Case 1
 * Hash = SHA-256
 */
static bool test_hkdf_rfc5869_case1(void) {
    /* Test vector from RFC 5869 A.1 */
    const uint8_t ikm[] = {
        0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
        0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
        0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b
    };
    const uint8_t salt[] = {
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
        0x08, 0x09, 0x0a, 0x0b, 0x0c
    };
    const uint8_t info[] = {
        0xf0, 0xf1, 0xf2, 0xf3, 0xf4, 0xf5, 0xf6, 0xf7,
        0xf8, 0xf9
    };

    const uint8_t expected_prk[] = {
        0x07, 0x77, 0x09, 0x36, 0x2c, 0x2e, 0x32, 0xdf,
        0x0d, 0xdc, 0x3f, 0x0d, 0xc4, 0x7b, 0xba, 0x63,
        0x90, 0xb6, 0xc7, 0x3b, 0xb5, 0x0f, 0x9c, 0x31,
        0x22, 0xec, 0x84, 0x4a, 0xd7, 0xc2, 0xb3, 0xe5
    };

    const uint8_t expected_okm[] = {
        0x3c, 0xb2, 0x5f, 0x25, 0xfa, 0xac, 0xd5, 0x7a,
        0x90, 0x43, 0x4f, 0x64, 0xd0, 0x36, 0x2f, 0x2a,
        0x2d, 0x2d, 0x0a, 0x90, 0xcf, 0x1a, 0x5a, 0x4c,
        0x5d, 0xb0, 0x2d, 0x56, 0xec, 0xc4, 0xc5, 0xbf,
        0x34, 0x00, 0x72, 0x08, 0xd5, 0xb8, 0x87, 0x18,
        0x58, 0x65
    };

    uint8_t prk[32];
    uint8_t okm[42];

    /* Test HKDF-Extract */
    if (!tls_hkdf_extract(TLS_HASH_SHA256, salt, sizeof(salt),
                          ikm, sizeof(ikm), prk)) {
        return false;
    }

    if (memcmp(prk, expected_prk, sizeof(expected_prk)) != 0) {
        return false;
    }

    /* Test HKDF-Expand */
    if (!tls_hkdf_expand(TLS_HASH_SHA256, prk, sizeof(prk),
                         info, sizeof(info), okm, sizeof(okm))) {
        return false;
    }

    if (memcmp(okm, expected_okm, sizeof(expected_okm)) != 0) {
        return false;
    }

    return true;
}

